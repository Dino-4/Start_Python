#_____________________Преобразование между int и float___________________

# Любое целое число int можно использовать там где ожидается float. Python сам сделает преобразование

# Для преобразования из float в int используется явное преобразование (руками)
num1 = 17.89
num2 = -13.56
num3 = int(num1)
num4 = int(num2)
print(num3)
print(num4)
# Обратите внимание, что преобразование чисел с плавающей точкой в целое производится с округлением в сторону нуля,
# то есть int(1.7) = 1, int(-1.7) = -1

print('\n\n\n')


