#____________________Цело численный тип данных_______________

# Целые числа в Python представлены типом данных int
# Явно указанное численное значение в коде программы называется целочисленным литералом.

n = 17  # целочисленный литерал
m = 7   # целочисленный литерал
#  Когда Python встречает целочисленный литерал, он создает объект типа int, хранящий указанное значение.




#_________________Преобразование строки в целое число__________________

num = int(input()) # преобразование считанной строки в целое число
print(num)
# В выше перечисленном примере не обязательно использовать input()

n = int('12345') # преобразование строки в целое число
# Если строка не является числом, то при преобразовании возникнет ошибка.
print(n)
print('\n\n\n')


#_________________Целочисленные операторы______________-

# Следующая программа демонстрирует все целочисленные операторы:
a = 13
b = 7

total = a + b
diff = a - b
prod = a * b
div1 = a / b
div2 = a // b
mod = a % b
exp = a ** b

print(a, '+', b, '=', total)
print(a, '-', b, '=', diff)
print(a, '*', b, '=', prod)
print(a, '/', b, '=', div1)
print(a, '//', b, '=', div2)
print(a, '%', b, '=', mod)
print(a, '**', b, '=', exp)
print('\n\n\n')



#_______________Символ разделить_____________

