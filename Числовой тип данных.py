#__________________Функции min() и max()____________
# Функции для определения минимального и максимального значения

# строки str с числовыми значения (int и float) сравнить нельзя!

a = max(3, 8, -3, 12, 9)
b = min(3, 8, -3, 12, 9)
c = max(3.14, 2.17, 9.8)
print(a)
print(b)
print(c)
print('\n\n\n')


#_______________Функция abs()________________

# Модулем (абсолютной величиной) положительного числа называется само число,
# модулем отрицательного числа называется противоположное ему число,
# модуль нуля – нуль.

print(abs(10))
print(abs(-7))
print(abs(0))
