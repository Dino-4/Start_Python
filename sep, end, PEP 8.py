# 2.
# Для разных команд print() можно задавать разные параметры sep и end.

arg1 = 'Hello'
sep1 = '_-_'
end2 = '+++'

print(arg1, 'everyone', sep=sep1, end='! ')
print('How', 'are', 'you', 'in', '2024?', sep=' ', end=end2)
print()
print()
# 3.
# Чтобы убрать все дополнительные выводимые символы, можно установить параметры sep и end команды print() как пустые строки ('').

print('a', 'b', 'c', sep='', end='')
print('d', 'e', 'f', sep='', end='')
print()
print()

# 4.
# Если после вывода данных нужно более одного перевода строки,
# то можно использовать следующий код:

print('Pyhton', end = '\n\n\n\n')
print('Pyhton')
print(end = '\n\n\n')

#_____________________Множественное присваивание

name, surname = 'Gustavo', 'Fring' #<<<-----
print('Имя:', name, 'Фамилия:', surname)
# В языке Python за одну инструкцию присваивания можно задавать значения сразу нескольким переменным.
# Этот код можно записать и так
name = 'Timur'
surname = 'Guev'
print('Имя:', name, 'Фамилия:', surname)
print()

# Если требуется считать текст с клавиатуры и присвоить его в качестве значения переменным,
# то можно написать так:
name, surname = input(), input()
print('Имя:', name, 'Фамилия:', surname)


