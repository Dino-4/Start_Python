#______________Необязательные параметры команды print

print('aa', 'bb', 'cc')

print('aa', 'bb', 'cc', sep='-')  # Этот параметр называется sep (separator – разделитель)
# По умолчанию параметр sep равен ' ' пробелу. Мы можем задавать любое значение.

plus = '+'
print('aa', 'bb', 'cc', sep=plus)
print()

#_______________Параметр end


print("Make America")
print("great again!")
                                   # Строки кода 17 и 18 являются эквивалентными строкам 20 и 21
print('Make America', end='\n')
print('great again!', end='\n')
# У команды print() есть параметр end, определяющий, что нужно добавить в конец вывода.
# По умолчанию параметр end равен символу перевода строки (\n)
print()

print("Make America", end='+')
print("great again!")
# Если перевод строки делать не нужно или требуется указать специальное окончание для вывода,
# то следует явно указать значение для параметра end (можем указать через переменную, как и с параметром sep).
plus = "+"
print("Make America", end=plus)
print("great again!")
print()

print('Make', end='@')
print('\n\n\n')

